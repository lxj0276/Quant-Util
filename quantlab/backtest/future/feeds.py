# -*- coding: utf-8 -*-
import pandas as pd


class Feed(object):
    def __init__(self, time, code, price, volume, interest, money, ask1, asize1, bid1, bsize1):
        self.time = pd.to_datetime(time)
        self.code = code
        self.price = float(price)
        self.volume = float(volume)
        self.interest = float(interest)
        self.money = float(money)
        self.ask1 = float(ask1)
        self.asize1 = float(asize1)
        self.bid1 = float(bid1)
        self.bsize1 = float(bsize1)